#include "ft_printf.h"

size_t	ft_dlen(int num)
{
	int	len;

	len = 0;
	if (num < 0)
	{
		num *= -1;
		len++;
	}
	while (num)
	{
		num /= 10;
		len++;
	}
	return (len);
}
